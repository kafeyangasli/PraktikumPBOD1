    private void jInsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jInsButtonActionPerformed
        // TODO add your handling code here:
        Integer addNIM = Integer.valueOf(jTF_NIM.getText().isEmpty() ? "-999999" : jTF_NIM.getText());
        String addNama = jTF_Nama.getText();
        
        if (addNama.isEmpty()) {
            setStatus("Nama tidak boleh kosong!");
        } else {
            jTF_NIM.setText("");
            jTF_Nama.setText("");
            String res = addNIM == -999999 ? service.add(addNama) : service.add(new Mahasiswa(addNIM, addNama));
            setStatus(res);
            if ("Success!".equals(res)) {
                refreshList();
            }
        }
    }//GEN-LAST:event_jInsButtonActionPerformed

    private void jUpdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jUpdButtonActionPerformed
        // TODO add your handling code here:
        Integer updNIM = Integer.valueOf(jTF_NIM.getText().isEmpty() ? "-999999" : jTF_NIM.getText());
        String updNama = addEscapeCharacters(jTF_Nama.getText());
        System.out.println(updNama);
        
        if (updNIM == -999999 || updNama.isEmpty()) {
            setStatus("Nama/NIM tidak boleh kosong!");
        } else {
            jTF_NIM.setText("");
            jTF_Nama.setText("");
            String res = service.update(new Mahasiswa(updNIM, updNama));
            setStatus(res);
            if ("Success!".equals(res)) {
                refreshList();
            }
        }
    }//GEN-LAST:event_jUpdButtonActionPerformed

    private void jDelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDelButtonActionPerformed
        // TODO add your handling code here:
        Integer deleteNIM = Integer.valueOf(jTF_NIM.getText().isEmpty() ? "-999999" : jTF_NIM.getText());
        
        if (deleteNIM == -999999) {
            setStatus("NIM tidak boleh kosong!");
        } else {
            jTF_NIM.setText("");
            jTF_Nama.setText("");
            String res = service.delete(new Mahasiswa(deleteNIM, ""));
            setStatus(res);
            if ("Success!".equals(res)) {
                refreshList();
            }
        }
        
    }//GEN-LAST:event_jDelButtonActionPerformed

