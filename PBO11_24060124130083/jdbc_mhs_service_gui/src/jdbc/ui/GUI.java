    private Color getStatusFG(String text) {
        return text.toLowerCase().contains("success") ? Color.GREEN : Color.RED;
    }
    
    private Color getStatusBG(String text) {
        return text.toLowerCase().contains("success") ? Color.decode("#010101") : Color.decode("#F2F2F2");
    }
    
    private void refreshList() {
        listMhs.clear();
        List<Mahasiswa> listData = service.getAll();
        for (Mahasiswa mhs : listData) {
            listMhs.addElement("%s (%d)".formatted(mhs.getNama(), mhs.getId()));
        }
        setStatus("Table successfully loaded!");
    }
    
    private void setStatus(String text) {
        jStatusLabel.setText(text);
        jStatusLabel.setForeground(getStatusFG(text));
    }
    
    private String addEscapeCharacters(String text) {
        String newText = text.replaceAll("'", "''");
        return newText;
    }
